using UnityEngine;

public class NetworkManagerPersist : MonoBehaviour
{
    private static NetworkManagerPersist _instance;

    private void Awake()
    {
        if (_instance != null && _instance != this)
        {
            Destroy(gameObject);
            return;
        }
        _instance = this;
        DontDestroyOnLoad(gameObject);
    }
}
